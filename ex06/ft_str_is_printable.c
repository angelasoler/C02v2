int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 0 && str[i] <= 31)
			return (0);
		else if (str[i] == 127)
			return (0);
		else
			i++;
	}
	return (1);
}
