char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	int	i;

	i = 0;
	while (n >= 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
		n--;
	}
	while (n >= 1)
	{
		dest[i] = '\0';
		i++;
		n--;
	}
	return (dest);
}
